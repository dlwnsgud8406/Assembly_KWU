INNER2_PADDING_LOOP ;second line~before low line padding	
	STR r1, [r5], #4
	LDR r1, [r0, #4]!
	ADD r7, r7, #1
	CMP r7, #63
	BLT INNER2_PADDING_LOOP
	
	MOV r7, #0

	STR r1, [r5], #4
	STR r1, [r5], #4
	LDR r1, [r0, #4]!
	ADD r6, r6, #1
	B CHECK_PADDING_LOOP
	

STOP; make last line padding 
	MOV r6, #0
	LDR r1, [r0, #-256]!
	STR r1, [r5], #4
