	BEQ SLEEP
	LDR r2, [r3], #4
	MOV r8, r1, LSL #1
	CMP r8, #0
	MOVEQ r9, #0
	BEQ	MUL_FINISH
	MOV r8, r2, LSL #1
	CMP r8, #0
	MOVEQ r9, #0
	BEQ MUL_FINISH
	
	; First : do extraction sign, exponent, and fraction bit + Prepend leading 1 to form mantissa
	; extract exponent
	MOV r9, r1, LSL #1	
	MOV r9, r9, LSR #24	 ; exponent of num1
	MOV r4, r2, LSL #1
	MOV r4, r4, LSR #24	 ; exponent of num2
	ADD r9, r9, r4;
	SUB r9, r9, #127
	; extract fraction
	MOV r11, r1, LSL #9
	MOV r11, r11, LSR #9 ; fraction of num1
	MOV r12, r2, LSL #9
	MOV r12, r12, LSR #9 	; fraction of num2
	; extract sign bit
	MOV r1, r1, LSR #31	 ; sign of num1
	MOV r2, r2, LSR #31  ; sign of num2
	EOR r1, r1, r2
	
	ADD r11, r11, cr ; add 1
	ADD r12, r12, cr ; add 1
	
	; Second : do Multiplication mantissas
	MOV r11, r11, LSL #6 ; r11 = A
	ADD r0, r11, r11 ; r0 = 2A
	MOV r11, r11, LSL #1 ; {X and X-1}
	
	MOV r2, #14

RADIX_4
	SUBS r2, r2, #1
	BEQ MUL_NOMALIZATION
				
	MOV r8, r12, LSL #29
	MOVS r8, r8, LSR #29 ; r8 = {X+1 and  X and  X-1}
	; so  Y = 3bit
	BEQ SHIFT

	CMP r8, #2
	BLE ADD_1A
	
	CMP r8, #4
	BLT ADD_2A
	BEQ SUB_2A
	
	CMP r8, #6
	BLE SUB_1A
	BGT SHIFT

SHIFT
	MOV r12, r12, LSR #2		
	MOV r4, r4, ASR #2 ; shift
	B RADIX_4	
ADD_1A
	ADD r4, r4, r11	; result + A
	MOV r4, r4, ASR #2 ; add 1A and shift
	MOV r12, r12, LSR #2	
	B RADIX_4
ADD_2A
	ADD r4, r4, r0 ; result + 2A
	MOV r4, r4, ASR #2 ; add 2A and shift
	MOV r12, r12, LSR #2
	B RADIX_4
SUB_1A
	SUB r4, r4, r11 ; result - A
	MOV r4, r4, ASR #2 ; add 1A and shift
	MOV r12, r12, LSR #2
	B RADIX_4
SUB_2A
	SUB r4, r4, r0 ; result - 2A
	MOV r4, r4, ASR #2 ; add 2A and shift
	MOV r12, r12, LSR #2
	B RADIX_4

	; Third : normalization
MUL_NOMALIZATION	
	CMP r4, #134217728	 ;compare 2^28
	MOVGE r4, r4, LSR #1; normalization if it is bigger than 10.xx
	ADDGE r9, r9, #1 ; add exponent
	
	MOV r8, r4, LSL #30
	MOV r8, r8, LSR #31
	CMP r8, #1
	
	; fourth : add sign, exponent, and fraction	
MUL_ADDING
	MOV r1, r1, LSL #31			; sign bit
	MOV r9, r9, LSL #23			; exponent bit
	MOV r4, r4, LSL #6
	MOV r4, r4, LSR #9			; fraction bit
	ADDEQ r4, r4, #1
	ADD r9, r1, r9				; add sign bit, exponent,
	ADD r9, r9, r4				; add fraction
MUL_FINISH
	B DOADD

DOADD
   MOV r4, r10, LSL #1
   CMP r4, #0
   MOVEQ r6, r9 ; if r4 is 0, result = num2
   BEQ FINISH
   MOV r4, r9, LSL #1
   CMP r4, #0
   MOVEQ r6, r10 ; if r4 is 0, result = num1
   BEQ FINISH
  
  ; First : do extraction sign, exponent, and fraction bit + add near 1 to form mantissa
	; exponent
   MOV r1, r10, LSL #1   
   MOV r1, r1, LSR #24 ; exponent of num1
   MOV r8, r9, LSL #1
   MOV r8, r8, LSR #24 ; exponent of num2
   
   ; fraction
   MOV r2, r10, LSL #9
   MOV r2, r2, LSR #9  ; fraction bit
   MOV r12, r9, LSL #9
   MOV r12, r12, LSR #9  ; fraction bit
   
   ; sign
   MOV r10, r10, LSR #31 ; sign bit
   MOV r9, r9, LSR #31 ; sign bit 
   
   ADD r2, r2, cr ; add near 1
   ADD r12, r12, cr ; add near 1
   
   ;  Second : compare exponents, shift smaller
   CMP r1, r8; compare r1, r8
   RSBLT r0, r1, r8 ; if r1<r8
   MOVLT r2, r2, LSR r0  ; shift mantissa of num1
   MOVLT r1, r8  ; Standard exponent
   SUBGT r0, r1, r8  ; if r0 > r1
   MOVGT r12, r12, LSR r0  ; shift mantissa of num2 
   
   ; Third : add or sub mantissa
   CMP r10, r9   
   BEQ ADDING_MANTISSA ; add mantissas
   
   CMP r2, r12  ; Sub mantissas
   BEQ FINISH ; Result = 0
   SUBLT r2, r12, r2  ; if r2<12 r2=r12-12
   MOVLT r10, r9 ; mantissa
   SUBGT r2, r2, r12 ;r2>r12 r2=r2-r12
   
ADDING_LOOP
   CMP r2, #16777216  ; 2^24
   MOVLT r2, r2, LSL #1 ; normalization
   SUBLT r1, r1, #1; sub
   BLT   ADDING_LOOP
   BGE   ADDING_ADDING
   
ADDING_MANTISSA
   ADD r2, r2, r12
   CMP r2, #16777216         ; 2^24 = 16777216
   MOVGE r2, r2, LSR #1      ; Normalize mantissa, if 10.xxxxxx
   ADDGE r1, r1, #1         ; Adjust exponent
      
ADDING_ADDING
   SUB r2, r2, cr ; sub near 1
   MOV r10, r10, LSL #31 ; Sign
   MOV r1, r1, LSL #23 ; Exponent
   ADD r6, r10, r1 ; add sign bit, exponent,
   ADD r6, r6, r2  ; add fraction
   
FINISH
   MOV r10, r6 ; As a result, r10 = r10 + r6
	B CHECK_STATE ; and save matrix
  
CHECK_STATE
	ADD r13, r13, #1 ; counting
	CMP r13, #3 ;if third calculate
	BEQ THIRD_SIXTH_CAL
	CMP r13, #6
	BEQ THIRD_SIXTH_CAL
	CMP r13, #9 ; if nineth calculate
	BEQ LAST_CAL
	B DOMUL ; if not 3, 6, 9 th calculate go MUL

