	str r10, [r7], #4 ; save
	LDR r1, [r5, #-532]! ; and paddied matrix offset-532
	LDR r2, [r3, #-36]! ; and second matrix offset-36
	ADD r14, r14, #1; and counting
	MOV r10, #0
	MOV r13, #0
	CMP r14, #32
	BEQ NEXT_ROW_CAL
	B DOMUL
